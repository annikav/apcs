// For loops
for (int i = 0; i < 10; i++) {
	System.out.print(i + " ");
}
/*
- i is the loop control variable
- can use any variable, but i is preferred; stands for index
- you can declare i in a variable above and not need to declare it there
- i = 0; initalizes loop control variable
- i < 10; tells it when to stop
- i++; tells it what to count by

for loop is used when...
- definite stating and stopping point
- when you know exactly how many times you want something to repeat
- use this for counting

array.length
// returns number of elements in an array
// NO parentheses (unlike string)
*/