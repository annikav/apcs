/*

Abstract Classes
- abstract is a todo list from a parent to a child
- abstract can modify a parent class
- cannot be instantiated (can't create a variable of that data type)
- some of the methods in the parent class are abstract
- abstract methods only have the header, no code inside

Parent class *defines* behavior by listing abstract methods
Child class *implements* behavior by writing out the full code for that method

Q: What if child class does not finish ALL of abstract methods from the parent class?
A: The class becomes abstract (inherits the abstraction)
- Abstraction only ends when all the methods have been written

*/